import mongoose from 'mongoose'
import crypto from 'crypto'

const ProductSchema = new mongoose.Schema({
  name: {
    type: String,
    trim: true,
    required: 'Name is required'
  },
  image: {
    data: Buffer,
    contentType: String
  },
  description: {
    type: String,
    trim: true
  },
  category: {
    type: String
  },
})

export default mongoose.model('Product', ProductSchema)
